section .data
    strd db 5,5,5,0,7,7,7,7   ; 3 dup(5), 0, 4 dup(7)
    h    dw -1, -2, -3         ; Определение слов (word) со значениями -1, -2, -3
    w    dq 0x100000, 100000   ; Определение четверных слов (dq): 100000h и 100000
    s    dd 1.0, -1.0          ; Определение вещественных чисел одинарной точности
    ; DT 5.0, -5.0             ; Пропущено (не поддерживается в NASM напрямую)

section .text
    global _start

_start:
    mov eax, 3                 ; Загружаем в EAX значение 3 (счетчик цикла)
cycle:
    mov [strd + eax - 1], al   ; Записываем младший байт AL в strd[EAX-1]
    dec eax                    ; Уменьшаем EAX на 1
    jnz cycle                  ; Если EAX != 0, переходим к метке cycle
    add dword [strd], 0x303030 ; Добавляем к первым 4 байтам strd значение "000" (ASCII)

    ; Вывод содержимого strd
    mov eax, 4                 ; Номер системного вызова sys_write
    mov ebx, 1                 ; Файловый дескриптор stdout
    mov ecx, strd              ; Адрес строки для вывода
    mov edx, 8                 ; Длина выводимых данных (8 байт)
    int 0x80                   ; Вызов ядра

    ; Завершение программы через системный вызов exit
    mov eax, 1                 ; Номер системного вызова (exit)
    mov ebx, 0                 ; Код возврата
    int 0x80                   ; Вызов ядра